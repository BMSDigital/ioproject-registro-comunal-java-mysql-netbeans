    private void cboaccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboaccederActionPerformed
     
        try{
          DefaultTableModel modelo;
          fusuario func = new fusuario();
          vusuario dts  = new vusuario();
          
          dts.setLogin(txtusuario.getText());
          dts.setPasword(txtpasword.getText());
          
          modelo = func.login(dts.getLogin(), dts.getPasword());
          tablalistado.setModel(modelo);
          
          if(func.totalregistros > 0){
             
             Ajustes.Aplicacion.callInicio();
             Ajustes.Aplicacion.onLoginEvent(Usuario);
             Bienvenida.lbidubicacion.setText(tablalistado.getValueAt(0, 0).toString());
             Bienvenida.lbnombre.setText(tablalistado.getValueAt(0, 1).toString());
             Bienvenida.lbapellido.setText(tablalistado.getValueAt(0, 2).toString());
             Bienvenida.lbnivel_acceso.setText(tablalistado.getValueAt(0, 3).toString());
             Bienvenida.lbparroquia.setText(tablalistado.getValueAt(0, 4).toString());
             Bienvenida.lbusuario.setText(tablalistado.getValueAt(0, 5).toString());
             Bienvenida.lbparroquia1.setText(tablalistado.getValueAt(0, 7).toString());
             
             if(!Bienvenida.lbnivel_acceso.getText().equals("Super Admin")){
                MenuPrincipal.archivo.setEnabled(false);
                MenuPrincipal.configuraciones.setEnabled(false);
             }
              
          }else{
             JOptionPane.showMessageDialog(null, "Acceso denegado","Acceso al Sistema",JOptionPane.ERROR_MESSAGE);
          }
          Ajustes.Aplicacion.repaintScreen();
       }catch(Exception e){
           JOptionPane.showConfirmDialog(null, e);
       }
    }//GEN-LAST:event_cboaccederActionPerformed

